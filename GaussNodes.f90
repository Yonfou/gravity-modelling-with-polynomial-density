
SUBROUTINE GaussNodes(NG1,AP1,TP1,NG2,AP2,TP2,AP0)
!-------------------------------------------------------------------------------------------

  IMPLICIT NONE

  INTEGER(KIND=4) :: NG1,NG2,II,JJ,KK,I0
  REAL   (KIND=8) :: AP1(NG1),TP1(NG1),AP2(NG2),TP2(NG2),AP0(NG1*NG2)
!-------------------------------------------------------------------------------------------  
  
  
  
                                    
                !-------------------------------------------------------------
                AP1(:)=0.D0;    TP1(:)=0.D0;    AP2(:)=0.D0;    TP2(:)=0.D0    
                !-------------------------------------------------------------
                AP0(:)=0.D0
                !-------------------------------------------------------------
                
                
                                                                                                   
                !-------------------------------------------------------------
                 IF(NG1==1) THEN
                    !---------------------------------------------------------
                    AP1(1)=2.D0;                 TP1(1)=0.D0
                    !---------------------------------------------------------
                ELSE IF(NG1==2) THEN
                    !---------------------------------------------------------
                    AP1(1)=1.D0;                 TP1(1)=-0.577350269189625D0
                    AP1(2)=1.D0;                 TP1(2)= 0.577350269189625D0
                    !---------------------------------------------------------
                ELSE IF(NG1==3) THEN
                    !---------------------------------------------------------
                    AP1(1)=0.555555555555555D0;   TP1(1)=-0.774596669241483D0
                    AP1(2)=0.888888888888888D0;   TP1(2)= 0.D0
                    AP1(3)=0.555555555555555D0;   TP1(3)= 0.774596669241483D0
                    !---------------------------------------------------------
                ELSE IF(NG1==4) THEN
                    !---------------------------------------------------------
                    AP1(1)=0.347854845137453D0;  TP1(1)=-0.861136311594052D0
                    AP1(2)=0.652145154862546D0;  TP1(2)=-0.339981043584856D0
                    AP1(3)=0.652145154862546D0;  TP1(3)= 0.339981043584856D0
                    AP1(4)=0.347854845137453D0;  TP1(4)= 0.861136311594052D0
                    !---------------------------------------------------------
                END IF
                !-------------------------------------------------------------
                
                
                
              
                !-------------------------------------------------------------
                IF(NG2==1) THEN
                    !---------------------------------------------------------
                    AP2(1)=2.D0;                 TP2(1)=0.D0
                    !---------------------------------------------------------
                ELSE IF(NG2==2) THEN
                    !---------------------------------------------------------
                    AP2(1)=1.D0;                 TP2(1)=-0.577350269189625D0
                    AP2(2)=1.D0;                 TP2(2)= 0.577350269189625D0
                    !---------------------------------------------------------
                ELSE IF(NG2==3) THEN
                    !---------------------------------------------------------
                    AP2(1)=0.555555555555555D0;   TP2(1)=-0.774596669241483D0
                    AP2(2)=0.888888888888888D0;   TP2(2)= 0.D0
                    AP2(3)=0.555555555555555D0;   TP2(3)= 0.774596669241483D0
                    !---------------------------------------------------------
                ELSE IF(NG2==4) THEN
                    !---------------------------------------------------------
                    AP2(1)=0.347854845137453D0;  TP2(1)=-0.861136311594052D0
                    AP2(2)=0.652145154862546D0;  TP2(2)=-0.339981043584856D0
                    AP2(3)=0.652145154862546D0;  TP2(3)= 0.339981043584856D0
                    AP2(4)=0.347854845137453D0;  TP2(4)= 0.861136311594052D0
                    !---------------------------------------------------------
                END IF
                !-------------------------------------------------------------
                
  
  
                !---------------------------------------------------------- 
                I0=0
                !---------------------------------------------------------- 
                DO JJ=1,NG1
                DO KK=1,NG2
                   !-------------------------------------------------------
                   I0=I0+1;    AP0(I0)=AP1(JJ)*AP2(KK)
                   !-------------------------------------------------------
                END DO
                END DO
                !---------------------------------------------------------- 
  
                
                
!-------------------------------------------------------------------------------------------
                                                                                    RETURN
END SUBROUTINE
