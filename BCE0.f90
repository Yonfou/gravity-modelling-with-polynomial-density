
SUBROUTINE BCE0(N,Colum,S,F)
!-------------------------------------------------------------------------------------------

  IMPLICIT NONE

  INTEGER(KIND=4) :: N
  REAL   (KIND=8) :: Colum(N),S(N),F(N)
  COMPLEX(KIND=8),ALLOCATABLE :: H(:),R(:)
  
!-------------------------------------------------------------------------------------------  
  
  
  
                                    
                !-------------------------------------------------------------
                ALLOCATE(H(2*N));                     H(:)=0.D0
                ALLOCATE(R(2*N));                     R(:)=0.D0
                !-------------------------------------------------------------
                
                
                
                                                                               
                !-------------------------------------------------------------
                H(1:N)=Colum(:);                    H(2*N:N+2:-1)=H(2:N)  
                !-------------------------------------------------------------
                R(1:N)=S(:)
                !-------------------------------------------------------------
                CALL FFT_1D(2*N,H,1)
                CALL FFT_1D(2*N,R,1)
                !-------------------------------------------------------------
                H=H*R;  CALL FFT_1D(2*N,H,-1)
                !-------------------------------------------------------------
                F(:)=H(1:N)
                !-------------------------------------------------------------
  
                           
                !-------------------------------------------------------------
                DEALLOCATE(H,R)
                !-------------------------------------------------------------
                
                
                
!-------------------------------------------------------------------------------------------
                                                                                    RETURN
END SUBROUTINE
