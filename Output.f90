
SUBROUTINE Output()
!-------------------------------------------------------------------------------------------
                                                                             USE ObserPoint
                                                                             USE Source
                                                                             USE GravityField
  IMPLICIT NONE

  INTEGER(KIND=4) :: I,J,N
  REAL   (KIND=8) :: Error(10)
!-------------------------------------------------------------------------------------------  
    
  
       
                                                                                               
                !-------------------------------------------------------------    
                OPEN(1,FILE=TRIM(ADJUSTL(file_rst)),STATUS='UNKNOWN') 
                    !---------------------------------------------------------
                    DO I=1,NPR
                    DO J=1,NPF
                    DO N=1,NPL
                       !------------------------------------------------------
                       WRITE(1,'(15(F30.18,F30.18))') PR(I),PF(J),PL(N),GV(I,J,N),& 
                                                    & GA(1,I,J,N),GA(2,I,J,N),GA(3,I,J,N),&
                                                    & GG(1,I,J,N),GG(2,I,J,N),GG(3,I,J,N),&
                                                    & GG(4,I,J,N),GG(5,I,J,N),GG(6,I,J,N)
                       !------------------------------------------------------
                    END DO
                    END DO
                    END DO
                    !---------------------------------------------------------
                CLOSE(1)
                !-------------------------------------------------------------
                
                
                
                

              
                
!-------------------------------------------------------------------------------------------
                                                                                    RETURN
END SUBROUTINE
